/** Marques normalisées (orthographe canonique), triées. */
export const ALL_BRANDS: string[] = (() => {
  const seen = new Set<string>();
  const result: string[] = [];
  for (const raw of RAW_BRANDS) {
    const normalized = BRAND_SPELLING_MAP[raw.toUpperCase()] ?? toTitleCase(raw);
    if (!seen.has(normalized) && normalized !== 'Autre') {
      seen.add(normalized);
      result.push(normalized);
    }
  }
  result.sort((a, b) => a.localeCompare(b, 'fr'));
  return result;
})();
